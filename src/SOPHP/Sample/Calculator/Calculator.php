<?php


namespace SOPHP\Sample\Calculator;


class Calculator {
    /**
     * @param float $a
     * @param float $b
     * @return float
     */
    public function add($a, $b) {
        return $a+$b;
    }

    /**
     * @param float $a
     * @param float $b
     * @return float
     */
    public function subtract($a, $b) {
        return $a-$b;
    }
} 